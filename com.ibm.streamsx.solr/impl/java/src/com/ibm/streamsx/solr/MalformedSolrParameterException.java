package com.ibm.streamsx.solr;

public class MalformedSolrParameterException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public MalformedSolrParameterException() {

	}

	public MalformedSolrParameterException(String arg0) {
		super(arg0);

	}

	public MalformedSolrParameterException(Throwable arg0) {
		super(arg0);

	}

	public MalformedSolrParameterException(String arg0, Throwable arg1) {
		super(arg0, arg1);

	}

	public MalformedSolrParameterException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);

	}

}
